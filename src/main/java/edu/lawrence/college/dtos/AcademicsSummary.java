package edu.lawrence.college.dtos;

public class AcademicsSummary {
	private Double avgRatio;
	private Double avgClassselection;
	private Double avgWorkload;
	private Double avgOpportunities;
	private Double avgAdvising;
	private Double avgLectures;
	public AcademicsSummary() {}
	
	public Double getAvgRatio() {
		return avgRatio;
	}

	public void setAvgRatio(Double avgRatio) {
		this.avgRatio = avgRatio;
	}

	public Double getAvgClassselection() {
		return avgClassselection;
	}

	public void setAvgClassselection(Double avgClassselection) {
		this.avgClassselection = avgClassselection;
	}

	public Double getAvgWorkload() {
		return avgWorkload;
	}

	public void setAvgWorkload(Double avgWorkload) {
		this.avgWorkload = avgWorkload;
	}
	public Double getAvgOpportunities() {
		return avgOpportunities;
	}

	public void setAvgOpportunities(Double avgOpportunities) {
		this.avgOpportunities = avgOpportunities;
	}

	public Double getAvgAdvising() {
		return avgAdvising;
	}

	public void setAvgAdvising(Double avgAdvising) {
		this.avgAdvising = avgAdvising;
	}

	public Double getAvgLectures() {
		return avgLectures;
	}

	public void setAvgLectures(Double avgLectures) {
		this.avgLectures = avgLectures;
	}

}
