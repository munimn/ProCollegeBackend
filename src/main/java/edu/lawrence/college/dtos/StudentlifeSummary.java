package edu.lawrence.college.dtos;

public class StudentlifeSummary {
	private Double avgCampusenv;
	private Double avgClubs;
	private Double avgGreeklife;
	private Double avgStudentorgs;
	private Double avgAdminhelp;
	private Double avgCafequality;
	public StudentlifeSummary() {}
	
	public Double getAvgCampusenv() {
		return avgCampusenv;
	}

	public void setAvgCampusenv(Double avgCampusenv) {
		this.avgCampusenv = avgCampusenv;
	}

	public Double getAvgClubs() {
		return avgClubs;
	}

	public void setAvgClubs(Double avgClubs) {
		this.avgClubs = avgClubs;
	}

	public Double getAvgGreeklife() {
		return avgGreeklife;
	}

	public void setAvgGreeklife(Double avgGreeklife) {
		this.avgGreeklife = avgGreeklife;
	}
	public Double getAvgStudentorgs() {
		return avgStudentorgs;
	}

	public void setAvgStudentorgs(Double avgStudentorgs) {
		this.avgStudentorgs = avgStudentorgs;
	}

	public Double getAvgAdminhelp() {
		return avgAdminhelp;
	}

	public void setAvgAdminhelp(Double avgAdminhelp) {
		this.avgAdminhelp = avgAdminhelp;
	}

	public Double getAvgCafequality() {
		return avgCafequality;
	}

	public void setAvgCafequality(Double avgCafequality) {
		this.avgCafequality = avgCafequality;
	}

}
